<h3><a class="mozTocH3" name="mozTocId810936"></a><a name="fernandogonzalez"></a>DATOS
BIOGR&Aacute;FICOS</h3>

<p>El se&ntilde;or FERNANDO GONZALEZ MESA naci&oacute; en
el Fresno el
31 de diciembre del a&ntilde;o 1.900, hijo del Se&ntilde;or
Rafael
Gonz&aacute;lez Ochoa y Salvadora Mesa T&eacute;llez.
Realiz&oacute;
sus estudios en el Fresno.</p>

<p>EI 18 de mayo del a&ntilde;o 1920 contrajo matrimonio con
Mar&iacute;a Rita Gonz&aacute;lez Jaramillo, de esta
uni&oacute;n hubo
9 hijos: Mar&iacute;a, Jorge, Salvadora, Alberto, Rosalba, Ana
In&eacute;s, Mercedes, Jes&uacute;s Dario y Beatriz.</p>

<p>Al contraer matrimonio inmediatamente tom&oacute;
posesi&oacute;n de
la finca ganadera denominada Carrizales, la cual fue heredada de su
padre. Poco a poco fueron llegando familias a trabajar en esta
regi&oacute;n y a ra&iacute;z de que el se&ntilde;or
Fernando
Gonz&aacute;lez Mesa hab&iacute;a implantado los cultivos de
caf&eacute;, ca&ntilde;a y cacao.</p>

<p>A estas familias se les donaban terrenos para que construyeran
sus casas y trabajaran como partigeros en su finca.</p>

<p>El Se&ntilde;or Fernando decidi&oacute; cambiar el
nombre de Carrizales por Betania, pues este le agradaba m&aacute;s.</p>

<p>Fue el primer presidente de Acci&oacute;n Comunal que tuvo
la
regi&oacute;n, desempe&ntilde;ando este cargo trajo el puesto
de
polic&iacute;as carabineros, luego se convirti&oacute; en el
primer
corregidor de la localidad.</p>

<p>Al ver que sus hijos y los de los trabajadores necesitaban
educaci&oacute;n decidi&oacute; construir una escuelita para
que la
comunidad se beneficiara de ella la cual qued&oacute; con el nombre
de
Escuela Rural Mixta Betania.</p>

<p>Su casa era muy frecuentada por las personas que habitaban el
lugar
y los que ven&iacute;an de otros lugares siempre encontraban en su
hogar un lugar para comer y dormir.</p>

<p>El se&ntilde;or Gonz&aacute;lez fue una persona que
contribuy&oacute; en los grandes avances que tuvo la
regi&oacute;n como fue:</p>

<p>La iniciaci&oacute;n del templo, v&iacute;as de
penetraci&oacute;n, viviendas y avances agr&iacute;colas entre
otros.</p>

<p>Fernando Gonz&aacute;lez Mesa fallece en Fresno el 3 de
diciembre
del a&ntilde;o 1967 dej&aacute;ndonos obras y un gran ejemplo
de
"progreso y convivencia&rdquo;.</p>

