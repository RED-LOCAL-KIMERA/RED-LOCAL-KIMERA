<h3><a class="mozTocH3" name="mozTocId747548"></a><a name="infraesctructura"></a>INFRAESTRUCTURA</h3>

<p><span style="font-weight: bold;">ELECTRIFICACION</span>.
Existe una
red de
electrificaci&oacute;n que presta un servicio deficiente, por su
reciente instalaci&oacute;n y por efecto de da&ntilde;os
ocasionados
por el invierno en localidades vecinas. El suministro de
energ&iacute;a
se hace a 20 casas del caser&iacute;o, pues la capacidad del
transformados instalado no alcanza a cubrir el total de viviendas y en
las fincas no se han hecho las instalaciones respectivas. Existen en
funcionamiento dos plantas de luz particulares que benefician casas
vecinas. <br />

<span style="font-weight: bold;">
ACUEDUCTO.</span> El caser&iacute;o dispone de un
peque&ntilde;o
acueducto
tomado de un nacimiento cercano, pero es deficiente por la falta de
conservaci&oacute;n y mantenimiento de los recursos naturales; la
desforestaci&oacute;n&nbsp; realizada en cercan&iacute;as
al nacimiento
de agua, ha mermado la capacidad de llenado del embalse, lo que con
frecuencia ocasiona la suspensi&oacute;n del servicio. <br />

La comunidad es propietaria de una Motobomba, que conectada a otra
fuente de Agua cercana, abastecer&iacute;a con suficiencia el
suministro, pero no la ponen a funcionar. En el resto de la
regi&oacute;n, (fincas) el agua llega por mangueras y gravedad en
algunos casos por F.V.C. o guadua.<br />

<span style="font-weight: bold;">
TELECOM.</span> Existe en la localidad una oficina de telecom,
con un servicio
excelente por el sistema de micro &ndash; ondas y discado directo a
cualquier parte del pa&iacute;s; su n&uacute;mero de llamada a
la
oficina para localizaci&oacute;n de personas, es 22405 Betania, y
se
puede discar directo con el indicativo de Mariquita. Tiene servicio de
Marconi por intermedio de Mariquita; la oficina es atendida por una
telefonista de lunes a domingo de 8 a 12 y de 2 a 6 p.m. <br />

<span style="font-weight: bold;">
ESCUELA.</span> La Educaci&oacute;n primaria se hace en los
grados de
primero a quinto con tres profesoras normalistas.<br />

La comunidad&nbsp; est&aacute; terminando de
construir&nbsp; un local funcionar&aacute; el primero de
bachillerato en 1987. <br />

<span style="font-weight: bold;">
INSPECCI&Oacute;N DE POLICIA.</span> Bajo el mando de un
Inspector y su
secretario,
funciona la inspecci&oacute;n&nbsp; de Polic&iacute;a, con
local
independiente de propiedad de la comunidad; en la actualidad no hay
agentes de polic&iacute;a.<br />

<span style="font-weight: bold;">
VIVIENDA Y OTRAS.</span> Se caracteriza la regi&oacute;n por
sus viviendas
en
una gran mayor&iacute;a en madera, con cocinas independientes; el
90%
no disponen de servicios sanitarios. <br />

El caser&iacute;o cuenta con Iglesia, y una cooperativa de consumo
sucursal de la Floresta. <br />

En la cabecera municipal cuenta con recursos de cr&eacute;dito a
trav&eacute;s de Caja Agraria, Banco Cafetero, pero se observa muy
baja
utilizaci&oacute;n de &eacute;stos. <br />

<span style="font-weight: bold;">
SALUD P&Uacute;BLICA.</span> Funciona un puesto de salud
atendido por una
enfermera,
pero su acondicionamiento en cuanto a drogas y equipo de
peque&ntilde;as cirug&iacute;as es deficiente. Ser&iacute;a
necesaria
una campa&ntilde;a de letrinizaci&oacute;n pues el 90% de las
viviendas
carecen de servicios sanitarios. <br />

Se ha presentado en la zona una enfermedad en la poblaci&oacute;n
infantil primordialmente, caracterizada por un brote en forma de
peque&ntilde;as vejigas, fiebre y escalofr&iacute;os;
&eacute;stos
casos han recibido atenci&oacute;n m&eacute;dica en el Fresno.</p>

