<h3><a class="mozTocH3" name="mozTocId651934"></a><a name="viasdeacceso"></a>VIAS DE ACCESO
Y TRANSPORTE</h3>

<p>A la vereda Betania se llega de Ibagu&eacute;, por
carretera pavimentada a Mariquita y luego
al&nbsp; Fresno; saliendo de Fresno hacia Manizales en el sitio
denominado Alto
de la Virgen en las goteras de Fresno, se desv&iacute;a a mano
derecha por un carreteable de penetraci&oacute;n en destapado, con
un
recorrido de 24 kms en el que los veh&iacute;culos de servicio
p&uacute;blico utilizan un tiempo de 2 a 2:30 horas hasta el
caser&iacute;o. El
tiempo total de recorrido desde Ibagu&eacute; es de 5 horas. <br />

El servicio de transporte se hace en camperos Carpati, que son los
encargados de sacar los productos al mercado; est&aacute;
establecida
una l&iacute;nea ordinaria que sale de Betania a las 6:00 a.m. y a
las
5:00 p.m. y del Fresno a las 9 a.m. y 3 p.m.; el carreteable
est&aacute; en p&eacute;simas condiciones. <br />

En la semana anterior se presentaron funcionarios de Caminos vecinales
para iniciar arreglos en la v&iacute;a Betania
&ndash;Mariquita, pero
no volvieron. Se habla de una v&iacute;a alterna por Betania a
empalmar
con la v&iacute;a a la Victoria Caldas como alternativa en
caso&nbsp;
de una eventual avalancha que destruya la v&iacute;a en Mariquita,
pero
esto no lo logramos confirmar con ninguna autoridad competente.</p>

