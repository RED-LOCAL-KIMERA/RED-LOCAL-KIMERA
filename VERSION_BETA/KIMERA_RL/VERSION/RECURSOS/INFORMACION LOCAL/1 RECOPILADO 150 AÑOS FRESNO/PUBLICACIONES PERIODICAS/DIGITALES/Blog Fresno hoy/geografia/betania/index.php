plantados, hay algunos mejorados con pasto brachiaria, imperial y
elefante, pero m&aacute;s son bien manejados. La comunidad no se
preocupa por alistar potreros ni proporciona buena
alimentaci&oacute;n
a sus animales, como consecuencia de los pocos conocimientos,
dificultad de consecuci&oacute;n de comillas y el escaso y
limitante
transporte.<br />

No hay instalaciones&nbsp; ser&aacute; las diferentes
explotaciones,&nbsp; si es que se les puede llamar as&iacute;,
en las
que hoy son muy deficientes.<br />

</p>

<h5><a class="mozTocH5" name="mozTocId111833"></a>
BOVINOS</h5>

<p>
En la regi&oacute;n al ganado fue desapareciendo paulatinamente
encontr&aacute;ndose ahora poca poblaci&oacute;n.<br />

Los potreros no reciben mantenimiento con los escasos
conocimientos&nbsp; y falta de tiempo porque lo dedican todo a
labores
culturales en el caf&eacute; o la ca&ntilde;a.<br />

La raza existente es ceb&uacute; y mestizos de ceb&uacute; como
BON; en
la regi&oacute;n es muy escasa y apetecida la leche, manifestando
los
propietarios su deseo de mejorar la producci&oacute;n, pero no
saben
como hacerlo t&eacute;cnicamente. <br />

Los animales complementan su alimentaci&oacute;n con plantas
leguminosas naturales que encuentran en los rastrojos; se presenta
sobre rastrojo por la falta de pastos y potreros. Se encuentran
helechos en gran cantidad pero no se presentan problemas de
intoxicaci&oacute;n, ni de enfermedades ya que la regi&oacute;n
es muy
sana.
</p>

<h5><a class="mozTocH5" name="mozTocId118731"></a>PORCINOS</h5>

<p>Hay buena
poblaci&oacute;n pero est&aacute; en malas condiciones de
manejo, pues los animales se tienen sueltos deambulando; su
alimentaci&oacute;n es a base de yuca cocinada y desechos&nbsp;
de la
alimentaci&oacute;n humana (c&aacute;scaras, residuos, etc).<br />

En la mayor&iacute;a de las fincas hay cerdos (de 1 a 5) no tienen
cochera, no se realizan pr&aacute;cticas de manejo
(desparasitaciones,
atenciones al parto, etc).<br />

El tiempo que demore un cerdo para salir al mercado es muy largo, al
final cuando est&aacute; cebado lo venden para ser sacrificado en
la
misma vereda.<br />

La raza predominante es zungo y mestizos de York &ndash; Shire; se
presentan pocos problemas infecciosos y enfermedades puesto que la
regi&oacute;n es muy sana y cuenta con aguas buenas.
</p>

<h5><a class="mozTocH5" name="mozTocId816776"></a>EQUINOS</h5>

<p>Los hay en gran cantidad
siendo utilizados para el trabajo (carne, molienda, transporte).<br />

Su alimentaci&oacute;n es base de ca&ntilde;a picada, cachasa
de
ca&ntilde;a, rastrojo y poco forraje, puesto que donde hay trapiche
por
lo general no hay potrero. <br />

Se presenta problemas de traumatismos y ca&iacute;da o
par&aacute;lisis
de los animales pero es consecuencia de la mala alimentaci&oacute;n
y
deficiencias minerales.
</p>

<h5><a class="mozTocH5" name="mozTocId880879"></a>AVES</h5>

<p>En el 98% de las fincas
tienen aves (pollos, gallinas, pavos, patos) en
cantidad m&iacute;nima de diez (10); no est&aacute;n explotados
t&eacute;cnicamente, se encuentran sueltos deambulando por las
vecindades, su alimentaci&oacute;n es a base de ma&iacute;z
complementado con pastos, rastrojo y animales silvestres.<br />

Son razas criollas y existe la afici&oacute;n por los gallos de
pelea.<br />

Es deseo de varias personas montar peque&ntilde;as explotaciones de
pollos de engorde y&nbsp; ponedoras que servir&iacute;an para
realizar
pr&aacute;cticas de aprendizaje veredal.
</p>

<h5><a class="mozTocH5" name="mozTocId796025"></a>RECOMENDACIONES
PECUARIAS</h5>

<p>Como ya se ha dicho, la
situaci&oacute;n en esta regi&oacute;n en
cuanto a t&eacute;cnicas es totalmente deficiente; por una parte,
la
ignorancia y por otra, que no han tenido ning&uacute;n apoyo de
entidades gubernamentales y del sector agropecuario.<br />

Se hacen necesarias labores de culturizaci&oacute;n,
difusi&oacute;n y
pr&aacute;cticas de manejo en bovinos, equinos y especies menores.<br />

Como el precio de la leche es elevado, se debe hacer un plan de
incremento lechero regional, as&iacute; habr&iacute;a mejor
nutrici&oacute;n en la alta densidad de poblaci&oacute;n
infantil, y
mayores fuentes de trabajo; sucede lo mismo con la carne, pues a cambio
de traer las reses para sacrificio de regiones lejanas,
deber&iacute;an
ser de la regi&oacute;n para disminuir los costos.<br />

Agilizar pr&eacute;stamos DRI, para que la comunidad pueda
conseguir su
vaca campesina, y establecer pastos especialmente de corte, ya que la
regi&oacute;n presenta buenas caracter&iacute;sticas para ello.<br />

Ense&ntilde;ar m&eacute;todos de construcci&oacute;n
f&aacute;cil y
barata de instalaciones agropecuarias, de tal manera que puedan
recolectarse deshechos y esti&eacute;rcol para la
elaboraci&oacute;n de
abonos org&aacute;nicos. <br />

En la regi&oacute;n hay condiciones &oacute;ptimas de clima y
suelos
para el establecimiento de explotaciones ganaderas, hacer planes de
semi &ndash; estabulaci&oacute;n, buscando el mejoramiento y
mayor
producci&oacute;n de los animales existentes.</p>

