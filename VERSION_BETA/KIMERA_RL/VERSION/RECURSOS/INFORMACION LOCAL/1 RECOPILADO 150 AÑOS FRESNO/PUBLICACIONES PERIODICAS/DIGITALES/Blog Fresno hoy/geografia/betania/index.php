<h3><a class="mozTocH3" name="mozTocId328991"></a><a name="explotacionagricola"></a>EXPLOTACIONES
AGR&Iacute;COLAS</h3>

<p><span style="font-weight: bold;">CULTIVO DEL
CAF&Eacute;</span>.
Es el cultivo
predominante de la regi&oacute;n y del cual derivan los habitantes
el
80% del sustento de sus familias; la variedad usada en la zona es
Caturra, encontr&aacute;ndose en bajo porcentaje plantaciones con
caf&eacute; ar&aacute;bigo ya en extinci&oacute;n.<br />

La producci&oacute;n de caf&eacute; seg&uacute;n los
cultivadores ha
venido a menos de tres a&ntilde;os a la fecha, por efectos de la
presencia de la ROYA en toda la zona, y las heladas que se han
presentado en el &uacute;ltimo semestre de 1985.<br />

Afect&oacute; tambi&eacute;n al cultivo la erupci&oacute;n
del
volc&aacute;n Nevado del Ruiz, en el follaje y fruto, lo mismo que
la
recolecci&oacute;n se dificult&oacute; por la arena que se
deposit&oacute; sobre las plantas y ca&iacute;a en los ojos de
los
trabajadores.<br />

No se realizan controles efectivos de fumigaci&oacute;n, por lo que
la
enfermedad ya est&aacute; muy avanzada, especialmente hacia
la&nbsp;
zona del ca&ntilde;&oacute;n del Guarin&oacute;. <br />

<span style="font-weight: bold;">
CA&Ntilde;A PANELERA.</span> Cultivo sembrado especialmente
en la zona
baja y
media de la vereda, con sistemas tradicionales de siembra y
recolecci&oacute;n por entresaque; la mezcla varietal, no permite
determinar la predominante en la zona; por lo que se observa en los
cultivos existentes, con buenas plantas que no han tenido un manejo
adecuado, es pata para una buena producci&oacute;n de panela;
aunque el
60% de los cultivadores no cuentan con montajes de procesamiento, lo
que disminuye sus ingresos al tener que recurrir al
&ldquo;partir&rdquo; con los que tienen montajes; aumentando el
&aacute;rea de cultivo de &eacute;stos a 10 o 12 has, se
justificar&iacute;a la financiaci&oacute;n de montajes,
incrementando
as&iacute; la productividad en la vereda.<br />

<span style="font-weight: bold;">
CACAO.</span> Sembrado a peque&ntilde;as cantidades en la
parte baja, sin
ninguna tecnificaci&oacute;n. Los agricultores manifiestan
desconocer
las mas elementales t&eacute;cnicas de labores culturales, pues
nunca
han tenido asesor&iacute;a en &eacute;ste cultivo.
Program&aacute;ndose
su incremento se rehabilitar&iacute;an las zonas bajas afectadas
por la
Roya. <br />

<span style="font-weight: bold;">
FRUTALES.</span> Hay en la regi&oacute;n dos cultivos de
papayo y
maracuy&aacute; que no reciben asesor&iacute;a;
&aacute;rboles de
aguacate y c&iacute;tricos plantados para el consumo familiar; por
las
caracter&iacute;sticas de la regi&oacute;n ser&iacute;an
los frutales
otro rengl&oacute;n promisorio, con el establecimiento de cultivos
comerciales y programas de huertos de frutales de diferentes especies;
por lo pronto hay tres agricultores interesados en huertos comerciales
&ndash; (mas de 1 Ha).<br />

<span style="font-weight: bold;">
YUCA.</span> Se siembra en forma intercalada con cafetales
soqueados y en
cantidades que no permiten su comercio, es decir para el consumo
familiar. Su incremento favorecer&iacute;a los ingresos. <br />

<span style="font-weight: bold;">
HORTALIZAS.</span> A excepci&oacute;n de algunos cultivos
comerciales de
tomate, el 100% de los agricultores desconocen su cultivo. Partiendo de
la capacitaci&oacute;n, su incremento se har&iacute;a
inicialmente a
nivel de huerta familiar, dado el alto n&uacute;mero de componentes
de
las familias y la disponibilidad de terreno adecuado para el
establecimiento en sus parcelas. <br />

Las escuelas de las veredas circunvecinas: Carrizales, Paramillo
&ndash; California y la Floresta, tienen lotes disponibles para la
realizaci&oacute;n de huertas escolares, que servir&aacute;n de
pr&aacute;ctica a los trabajadores alumnos de realizarse el curso
de
explotaciones diversificadas.<br />

<span style="font-weight: bold;">
PLATANO.</span> Cultivo realizado desordenadamente dentro de las
plantaciones
de caf&eacute;, sin mucha atenci&oacute;n a su sostenimiento,
por lo
que su duraci&oacute;n y producci&oacute;n es baja.<br />

<span style="font-weight: bold;">
MAIZ.</span> Su siembra se hace intercalada con caf&eacute; o
yuca pero en
peque&ntilde;a escala; generalmente no utilizan semillas de
h&iacute;bridos para la altura de la zona, sino residuos de
cosechas
anteriores, lo que hace bajo su rendimiento.</p>

<h3><a class="mozTocH3" name="mozTocId360862"></a>RECOMENDACIONES
GENERALES EN AGRICULTURA</h3>

<p>Es preocupante la
situaci&oacute;n en
cuanto los estragos que est&aacute; causando la ROYA en la
regi&oacute;n, puesto que &eacute;stos cultivadores
inobjetablemente
tendr&aacute;n que prescindir de su cultivo principal, y el lapso
de
tiempo de establecimiento de otros, al inicio de su
producci&oacute;n,
har&aacute; dif&iacute;cil la situaci&oacute;n de la zona a
un
t&eacute;rmino de 2 a 5 a&ntilde;os.<br />

Se har&iacute;a necesaria la capacitaci&oacute;n en los
cultivos
anteriormente mencionados como promisorios, y el pronto inicio de su
incremento y montaje de explotaciones a nivel comercial y familiar.<br />

El fomento de la ca&ntilde;a panelera, motivar&aacute; la
construcci&oacute;n de montajes y hasta la necesidad de proyectos
de
trapiches comunales.<br />

Cultivos de pan coger fundamentales en la alimentaci&oacute;n, como
el
ma&iacute;z y el frijol, se deber&iacute;an intensificar por
los
sistemas de siembras m&uacute;ltiples o intercaladas.<br />

Las hortalizas y los frutales ser&iacute;an renglones
&oacute;ptimos
para generar ingresos adicionales y rebaja de costos en el presupuesto
familiar.<br />

Como limitante en la regi&oacute;n se tiene el problema de la
hormiga
arriera, haci&eacute;ndose necesaria una campa&ntilde;a de
erradicaci&oacute;n general, en coordinaci&oacute;n con las
entidades
del sector agropecuario-<br />

En la diversificaci&oacute;n de cultivos es limitante, la
consecuci&oacute;n por parte de los agricultores de material
vegetal
adecuada para plantaciones de cacao, frutales y semillas.</p>

