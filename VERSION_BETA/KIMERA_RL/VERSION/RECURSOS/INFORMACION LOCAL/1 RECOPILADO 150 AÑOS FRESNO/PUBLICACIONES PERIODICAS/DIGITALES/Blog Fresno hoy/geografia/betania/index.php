<h3>HISTORIA
</h3>
<p>En el a&ntilde;o 1850,
proveniente de
Envigado Antioquia, lleg&oacute; el Sr. FAUSTINO GONZALEZ OCHOA, en
busca de tierras para trabajar; inducido por habitantes del entonces
corregimiento de Fresno, inspeccion&oacute; tierras
bald&iacute;as
hacia el Norte del Corregimiento, buscando el r&iacute;o
Guarin&oacute;, l&iacute;mite natural con el Departamento de
Caldas. <br />

Despu&eacute;s de registrar a su nombre la zona explorada, hizo
venir a
sus padres y hermanos: Rafael, Carlos, Luis Mar&iacute;a y
Bernardino
Gonz&aacute;lez Ochoa, con sus respectivas familias, repartiendo
con
ellos la regi&oacute;n; a medida que sus hijos se fueron
independizando, se diseminaron en fincas por la zona, por lo que
&eacute;sta se conoci&oacute; durante mucho tiempo como la
GONZALERA&rdquo;. <br />

A ra&iacute;z de la guerra de los mil d&iacute;as y a partir de
1886,
las consecuencias de &eacute;sta violencia, oblig&oacute; a
varios de
los descendientes a emigrar, y se presentaron asentamientos de nuevas
familias que fueron comprando terrenos. <br />

Los Gonz&aacute;lez organizaron una gran finca ganadera que
denominaron
Carrizales, conociendo as&iacute; la regi&oacute;n por mas de
una
d&eacute;cada. <br />

El cultivo predominante en &eacute;se entonces&nbsp; era el
ma&iacute;z
y el pl&aacute;tano; con la introducci&oacute;n del
caf&eacute;, los
Gonz&aacute;lez vincularon nuevas familias como agregados
&oacute;
partijeros&nbsp; para que sembraran el promisorio cultivo;
revelados
por la explotaci&oacute;n de que eran objeto, estos partijeros se
apropiaron y compraron las tierras trabajadas, por lo que la
egemon&iacute;a&nbsp; de los Gonz&aacute;lez
entr&oacute; en decadencia.<br />

Posteriormente el Sacerdote del Fresno en &eacute;sa
&eacute;poca,
Presb&iacute;tero Buenaventura Hauregui, en la primera misa que
ofici&oacute; en la regi&oacute;n propuso el nombre de BETANIA,
con el
que se conoce en la actualidad. El se&ntilde;or <a href="#fernandogonzalez">Fernando
Gonz&aacute;lez</a>, hijo de Rafael Gonz&aacute;lez
Ochoa,
reparti&oacute;
solares para fundar el caser&iacute;o, la escuela y la Iglesia. <br />

En la actualidad el caser&iacute;o cuenta con Escuela, Iglesia,
Sal&oacute;n comunal, Telecom, Acueducto,
Electrificaci&oacute;n a 20
casas, puesto de salud y una sucursal de la cooperativa de consumidores
de la&nbsp; Floresta.</p>

