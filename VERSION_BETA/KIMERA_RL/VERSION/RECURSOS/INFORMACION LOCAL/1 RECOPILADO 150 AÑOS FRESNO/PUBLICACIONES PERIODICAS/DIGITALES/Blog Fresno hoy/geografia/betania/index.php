de la carne que es comprada en el caser&iacute;o, pues matan dos
reses
los viernes y dos cerdos los mi&eacute;rcoles de cada semana.<br />

La panela la sacan al Fresno y en algunos casos dependiendo del precio
y la facilidad de transporte a Mariquita. <br />

Los pocos veh&iacute;culos&nbsp; y el mal estado de la
carretera en
&eacute;pocas de invierno, son limitantes para un adecuado mercadeo.</p>

