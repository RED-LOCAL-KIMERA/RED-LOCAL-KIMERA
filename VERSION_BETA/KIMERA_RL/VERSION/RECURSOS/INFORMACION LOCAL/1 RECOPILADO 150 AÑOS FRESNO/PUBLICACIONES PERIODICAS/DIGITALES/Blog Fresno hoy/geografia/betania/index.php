<h3><a class="mozTocH3" name="mozTocId710903"></a><a name="usotenenciadelatierra"></a>USO Y
TENENCIA DE LA TIERRA</h3>

<p>El promedio de
distribuci&oacute;n de la
tierra est&aacute; comprendido entre 1 y 25 Has por familia, y que
son
en su mayor&iacute;a propietarios: el mayor porcentaje de
&eacute;stas
tierras est&aacute; dedicado al cultivo del caf&eacute;; se
observa en
la zona baja, hacia el can&oacute;n de Guarin&oacute;,
presencia de
caceo en algunas fincas, pero sin ser explotaciones
t&eacute;cnicamente
orientadas.<br />

En todas las fincas de 8 Has en adelante, tanto en la parte baja y
alta, se encuentran extensiones de 1 a 5 Has de rastrojos, que
podr&iacute;an ser destinadas a otros cultivos y a&uacute;n en
la
instalaci&oacute;n de potreros para el incremento de la
ganader&iacute;a.<br />

Sigue en importancia al caf&eacute;, la ca&ntilde;a panelera,
explotada
con los sistemas tradicionales de siembra, recolecci&oacute;n
(entresaque), y beneficio, con muy pocos montajes, lo que hace que la
rentabilidad se disminuya para los cultivadores.</p>

