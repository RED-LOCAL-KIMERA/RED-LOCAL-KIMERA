<h3><a class="mozTocH3" name="mozTocId761515"></a><a name="ugeografica"></a>UBICACI&Oacute;N
GEOGR&Aacute;FICA</h3>

<p>La vereda Betania
est&aacute; ubicada al
Norte del Departamento del Tolima y al Nor &ndash; occidente de la
cabecera del municipio de Fresno. Los l&iacute;mites de su
jurisdicci&oacute;n son: por el norte con el r&iacute;o
Guarin&oacute;,
Depto. De Caldas, Municipio de Marquetalia; por el Sur con la Vereda
Paramillo, Caja Rica y la Floresta del Municipio de Fresno. Por el
Oriente con las veredas Carrizales y Pitalito del Municipio de
Mariquita, y por el occidente con las veredas Pavas, california y el
R&iacute;o Guarin&oacute;. <br />

<span style="font-weight: bold;">TOPOGRAFIA</span>:&nbsp;&nbsp;&nbsp;
Quebrada<br />

<span style="font-weight: bold;">ALTURA:</span>&nbsp;&nbsp;&nbsp;
1.600 m.s.n.m. en la parte alta,
900 m.s.n.m.
con la parte baja hacia el can&oacute;n del r&iacute;o
Guarin&oacute;. <br />

<span style="font-weight: bold;">
TEMPERATURA:</span>&nbsp;&nbsp;&nbsp; 18 a 24
&deg;C en
promedio. <br />

<span style="font-weight: bold;">
LLUVIAS:</span>&nbsp;&nbsp;&nbsp; Primer semestre
&ndash;
Abril mayo; Segundo semestre: octubre&nbsp; - noviembre. <br />

Por informaci&oacute;n de los
agricultores, se han
presentado variaciones notorias en los &uacute;ltimos dos
a&ntilde;os,
lo que ha desorientado y afectado las &eacute;pocas tradicionales
de
siembra y recolecci&oacute;n de productos. <br />

<span style="font-weight: bold;">
ALTERACIONES CLIMATICAS.</span> En el &uacute;ltimo semestre
/85 y lo que
va
del 86, se han presentado fuertes heladas que han propiciado
ca&iacute;da de frutos en caf&eacute;, aguacate, cacao.</p>

