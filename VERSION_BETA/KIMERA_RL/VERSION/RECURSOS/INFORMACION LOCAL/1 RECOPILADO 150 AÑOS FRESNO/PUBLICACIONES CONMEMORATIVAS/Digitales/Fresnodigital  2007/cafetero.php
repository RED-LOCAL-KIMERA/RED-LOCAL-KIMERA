<h2>EL SECTOR CAFETERO EN FRESNO</h2>


<p>El caf&eacute;, a pesar de las viscicitudes que ha tenido su
cultivo,&nbsp; ha jugado un papel importante durante toda la
historia del municipio.&nbsp; &nbsp;Hoy en d&iacute;a sigue siendo un
motor de desarrollo y le ha permitido a&nbsp;Fresno tomar un liderazgo
regional entre
lo que vale la pena resaltar: </p>


<ul>


  <li>La existencia de unas organizaciones que promueven esta
actividad, tales como el comite de cafeteros y varias&nbsp;
cooperativas y asociaciones de productores.</li>


  <li>Las iniciativas en marcha para la producci&oacute;n de
caf&eacute;s especiales y caf&eacute; org&aacute;nico, las cuales han
superado ya las etapas de promoci&oacute;n e inicio y se encuentran ya
produciendo y vendiendo sus productos. </li>


  <li>La intenci&oacute;n de modernizar la producci&oacute;n cafetera
reflejado en la presencia del proyecto jovenes cafeteros que tiene ya
en marcha varias fincas en el sector del Guayabo.</li>


  <li>Los esfuerzos en la parte educativa por parte del IET de la
vereda el Guayabo con el programa de computador para la
ense&ntilde;anza del cultivo del caf&eacute; y otros como el de don
Victor G&oacute;mez en la ense&ntilde;anza de la agricultura
org&aacute;nica.</li>


</ul>


<p style="text-align: justify;">En este contexto, queremos&nbsp;
preguntarnos hasta qu&eacute; punto lo digital
permitir&iacute;a&nbsp;tomar estos esfuerzos de liderazgos
individuales,&nbsp; divulgarlos,&nbsp; apoyar&nbsp;su
integraci&oacute;n y darles nuevas opciones de desarrollo.</p>


