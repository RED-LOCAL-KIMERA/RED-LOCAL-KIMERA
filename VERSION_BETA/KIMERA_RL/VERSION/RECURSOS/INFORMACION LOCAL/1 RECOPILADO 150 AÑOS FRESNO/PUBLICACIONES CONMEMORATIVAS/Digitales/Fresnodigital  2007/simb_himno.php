orgullo de mi Tolima.</p>

<p style="text-align: justify;">El cuerpo propiamente
dicho de la
composici&oacute;n lo conforman dieciseis (16) versos divididos en
cuatro (4) estrofas cada una con un intencionalidad. &nbsp;La
primera
hace referencia a la situaci&oacute;n geogr&aacute;fica (la
cordillera)
y al ancestro arriero. &nbsp;La segunda describe algunas
peculiaridades
del paisaje rodeado por una cadena de monta&ntilde;as. &nbsp;El
Fresno
est&aacute; encerrado y ma&ntilde;an y tarde se ve subir la
brisa
(niebla) del Magdalena que e da ese apecto brumoso del que se queja el
visitante. &nbsp;La terdera nos recuerda que miles de fresnenses no
viven ya en la poblaci&oacute;n, se han ido buscando otras
oportunidades pero que el pueblo siempre es la referencia de recuerdos
gratos y amables, las muchachas bonitas, las calles. La cuarta y
&uacute;ltima estrofa nos devuelve el alma arriera, buscadores
permanentes, trabajadores incansables. &nbsp;Es el paradigma de los
arrieros.</p>

